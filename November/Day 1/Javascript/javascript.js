function reverse(s){
    return s.split("").reverse().join("");
}

var string1 = reverse("adam");
var string2= reverse("John!");
var apple= reverse("apple");
var hello= reverse("hello");
var greetings= reverse("Greetings!");
console.log(string1);
console.log(string2);
console.log(apple);
console.log(hello);
console.log(greetings);
